package String;

public class StringCreation {
	public static void main(String[]args)
	{
		String s1="java";
		String s2=new String("Python");
		char[] c= {'S','Q','L'};
		String s3=new String(c);
		System.out.println("s1="+s1);
		System.out.println("s2="+s2);
		System.out.println("s3="+s3);
	}


}
